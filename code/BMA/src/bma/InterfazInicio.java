private boolean isInteger(String cadena){
    try{
        int num;
        num=Integer.parseInt(cadena);
        return true;
    }catch(Exception e){
        return false;
    } 
}
private boolean isLong(String cadena){
    try{
        Long num;
        num=Long.parseLong(cadena);
        return true;
    }catch(Exception e){
        return false;
    } 
}

