    public void actualizaTablaGrupos() throws SQLException {
        List<List<String>> grupos = GestorGrupos.getListaGrupos(this.bd);

        String aux;
        Object[][] data = new Object[grupos.size()][5];
        for (int i = 0; i < grupos.size(); i++) {
            aux = grupos.get(i).get(0);
            data[i][0] = aux.substring(0, aux.indexOf(","));
            aux = aux.substring(aux.indexOf(",") + 1, aux.length());
            data[i][1] = aux.substring(0, aux.indexOf(","));
            aux = aux.substring(aux.indexOf(",") + 1, aux.length());
            data[i][2] = getCategoria(aux.substring(0, aux.indexOf(",")));
            aux = aux.substring(aux.indexOf(",") + 1, aux.length());
            data[i][3] = getEntrenador(aux.substring(0, aux.indexOf(",")));
            aux = aux.substring(aux.indexOf(",") + 1, aux.length());
            data[i][4] = getTemporada(aux);
        }
        
        DefaultTableModel dtm = new DefaultTableModel(data, new String[] { "ID Grupo", "Numero Alumnos", "Categoria", "Entrenador", "Temporada"}) {
            Class[] types = new Class[] { java.lang.String.class, java.lang.String.class, java.lang.String.class, java.lang.String.class, java.lang.String.class};
            boolean[] canEdit = new boolean[] { false, false, false, false, false };

            @Override
            public Class getColumnClass(int columnIndex) {
                return types [columnIndex];
            }

            @Override
            public boolean isCellEditable(int rowIndex, int columnIndex) {
                return canEdit [columnIndex];
            }
        };

        tablaGrupos.setModel(dtm);
        
    }
