    private void botonConsGrupoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonConsGrupoActionPerformed
        int filaSel = tablaGrupos.getSelectedRow();
        if (filaSel == -1) {
            JOptionPane.showMessageDialog(this, "Debes seleccionar un grupo", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            String idGrupo = (String) tablaGrupos.getValueAt(filaSel, 0);

            try {
                new ConsultarGrupo(this, idGrupo).setVisible(true);
            } catch (SQLException ex) {
                Logger.getLogger(PantallaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_botonConsGrupoActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        int filaSel = tablaGrupos.getSelectedRow();
        if (filaSel == -1) {
            JOptionPane.showMessageDialog(this, "Debes seleccionar un grupo", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            int continuar = JOptionPane.showConfirmDialog(this, "Â¿Desea eliminar el grupo?", "Confirmar", JOptionPane.YES_NO_OPTION);
            if (continuar == JOptionPane.YES_OPTION) {
                int idCat = 0, idEnt = 0, idInst = 0, idTemp = 0;
                String temp = "";

                int idGrup = Integer.parseInt((String) tablaGrupos.getValueAt(filaSel, 0));
                String cat = (String) tablaGrupos.getValueAt(filaSel, 2);
                String ent = (String) tablaGrupos.getValueAt(filaSel, 3);
                String inst = (String) tablaGrupos.getValueAt(filaSel, 4);

                try {
                    idCat = GestorCategorias.getIdCategoria(this.bd, cat);
                    idEnt = GestorUsuarios.getIdEnt(this.bd, ent);
                    idInst = GestorInstalacion.getIdInstalacion(this.bd, inst);
                    idTemp = GestorGrupos.getIdTemporada(this.bd, idGrup);
                    temp = GestorTemporadas.getTemporada(this.bd, idTemp);
                } catch (SQLException ex) {
                    Logger.getLogger(PantallaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
                }

                Grupo g = new Grupo(cat, ent, inst, temp);
                g.setIdGrupo(idGrup);
                g.setIdCat(idCat);
                g.setIdEnt(idEnt);
                g.setIdInst(idInst);
                g.setIdTemp(idTemp);

                boolean GrupoEliminado = false;

                try {
                    GrupoEliminado = GestorGrupos.EliminarGrupos(this.bd, g);
                } catch (SQLException ex) {
                    Logger.getLogger(PantallaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
                }

                if (GrupoEliminado) {
                    JOptionPane.showMessageDialog(this, "Grupo Eliminado con exito", "Exito", JOptionPane.NO_OPTION);

                    try {
                        actualizaTablaGrupos();
                    } catch (SQLException ex) {
                        Logger.getLogger(PantallaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "No se ha podido borrar el grupo", "Error", JOptionPane.ERROR_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_jButton5ActionPerformed

